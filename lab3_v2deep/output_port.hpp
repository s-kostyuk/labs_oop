/*****************************************************************************/

#ifndef _OUTPUT_PORT_HPP_
#define _OUTPUT_PORT_HPP_

/*****************************************************************************/

#include "port.hpp"

/*****************************************************************************/

class Element;

/*****************************************************************************/

class OutputPort : public Port {

	/*-----------------------------------------------------------------*/

public:

	/*-----------------------------------------------------------------*/

	OutputPort( const std::string _portName );
	~OutputPort() override = default;

	/*-----------------------------------------------------------------*/

	bool getValue() const override;

	void setInput( const Element * _pTarget );
	void unsetInput();

	/*-----------------------------------------------------------------*/

private:

	/*-----------------------------------------------------------------*/

	const Element * m_pTarget;

	/*-----------------------------------------------------------------*/

};

/*****************************************************************************/

inline void OutputPort::setInput( const Element * _pTarget ) {
	m_pTarget = _pTarget;
}

/*****************************************************************************/

inline void OutputPort::unsetInput() {
	m_pTarget = nullptr;
}

/*****************************************************************************/

#endif //_OUTPUT_PORT_HPP_
