/*****************************************************************************/

#ifndef _INPUT_PORT_HPP_
#define _INPUT_PORT_HPP_

/*****************************************************************************/

#include "port.hpp"

/*****************************************************************************/

class InputPort : Port {

	/*-----------------------------------------------------------------*/

public:

	/*-----------------------------------------------------------------*/

	InputPort( std::string & _portName );

	~InputPort() override = default;

	/*-----------------------------------------------------------------*/

	InputPort ( const InputPort & _p ) = delete;
	InputPort & operator = ( const InputPort & _p ) = delete;

	/*-----------------------------------------------------------------*/

	bool getValue() const override;

	void setValue( bool _value );

	/*-----------------------------------------------------------------*/

private:

	/*-----------------------------------------------------------------*/

	bool m_value;

	/*-----------------------------------------------------------------*/

};

/*****************************************************************************/

inline InputPort::InputPort( std::string & _portName )
		:   Port( _portName )
		,   m_value( false )
{ }

/*****************************************************************************/

inline bool InputPort::getValue() const {
	return m_value;
}

/*****************************************************************************/

inline void InputPort::setValue( bool _value ) {
	m_value = _value;
}

/*****************************************************************************/

#endif //_INPUT_PORT_HPP_
