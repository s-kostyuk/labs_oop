/*****************************************************************************/

#ifndef _BINARY_ELEMENT_HPP_
#define _BINARY_ELEMENT_HPP_

/*****************************************************************************/

#include "element.hpp"

/*****************************************************************************/

class BinaryElement : public Element {

	/*-----------------------------------------------------------------*/

public:

	/*-----------------------------------------------------------------*/

	enum class Type {
		AND, OR, XOR
	};

	/*-----------------------------------------------------------------*/

	BinaryElement( const Type _t, const Element * const _input1, const Element * const _input2 );
	~BinaryElement() override = default;

	/*-----------------------------------------------------------------*/

	Type getType();

	/*-----------------------------------------------------------------*/

	bool evaluate() const override;

	/*-----------------------------------------------------------------*/

private:

	/*-----------------------------------------------------------------*/

	const Type m_type;

	const Element * const m_input1;
	const Element * const m_input2;

	/*-----------------------------------------------------------------*/

};

/*****************************************************************************/

inline BinaryElement::Type BinaryElement::getType() {
	return m_type;
}

/*****************************************************************************/

#endif //_BINARY_ELEMENT_HPP_
